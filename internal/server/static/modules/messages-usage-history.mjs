function lookup(catalog, key) {
  return String(key || "").split(".").reduce((value, part) => (
    value && typeof value === "object" ? value[part] : undefined
  ), catalog);
}

export function usageHistoryMessage(key, params = {}, locale = "zh-CN") {
  const catalog = usageHistoryMessages[locale] || usageHistoryMessages["zh-CN"];
  const message = lookup(catalog, key) ?? lookup(usageHistoryMessages["zh-CN"], key) ?? key;
  return String(message).replace(/\{([A-Za-z0-9_]+)\}/g, (match, name) => (
    Object.prototype.hasOwnProperty.call(params, name) ? String(params[name] ?? "") : match
  ));
}

export default usageHistoryMessages;
